    public boolean isvalid(){
        if(txtMasp.getText().length() == 0){
            DialogHelper.alert(this, "Không được bỏ trống mã sản phẩm!");
            return false;
        }
        if(txtMasp.getText().length()< 2){
            DialogHelper.alert(this, "Mã sản phẩm có ít nhất có 2 ký tự!");
            return false;
        }
//        if(txtGia.getText()){
//            DialogHelper.alert(this, "Giá tiền phải nhập bằng số!");
//            return false;
//        }
        return true;
    }
    
    void load() {
        DefaultTableModel model = (DefaultTableModel) tblGirlview.getModel();
        model.setRowCount(0);
        try {
            List<SanPham> list = dao.select();
            for (SanPham tl : list) {
                Object[] row = {
                tl.getMaSP(),
                tl.getTenSP(),
                tl.getHinh(),
                tl.getdVT(),
                tl.getNuocSX(),
                tl.getGia(),
                tl.getGhiChu()
            };
            model.addRow(row);
            }
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    public boolean insert(){
        SanPham model = getModel();
        try {
            dao.insert(model);
            this.load();
            this.clear();
            DialogHelper.alert(this, "Thêm mới thành công!");
            return true;
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Thêm mới thất bại!");
        }
        return false;
    }
    
    public boolean update(){
        SanPham model = getModel();
        try {
            dao.update(model);
            this.load();
            DialogHelper.alert(this, "Cập nhật thành công!");
            return true;
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Cập nhật thất bại!");
        }
        return false;
    }

    public boolean delete(){
        if(DialogHelper.confirm(this, "Bạn có muốn xóa hay không?")){
            String macd = txtMasp.getText();
            try {
                dao.delete(macd);
                this.load();
                this.clear();
                DialogHelper.alert(this, "Xóa thành công!");
                return true;
            }
            catch (Exception e) {
                DialogHelper.alert(this, "Xóa thất bại!");
            }
        }
        return false;
    }

    void clear(){
        this.setModel(new SanPham());
        this.setStatus(true);
    }

    void edit() {
        try {
            String macd = (String) tblGirlview.getValueAt(this.index, 0);
            SanPham model = dao.findById(macd);
            if(model != null){
            this.setModel(model);
            this.setStatus(false);
            }
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    void setModel(SanPham model){
        txtMasp.setText(model.getMaSP());
        txtTensp.setText(model.getTenSP());
        txtDvt.setText(model.getdVT());
        txtNsx.setText(model.getNuocSX());
        txtGia.setText(model.getGia());
        txtMota.setText(model.getGhiChu());
        lblDVT.setText(null);
        lblGia.setText(null);
        lblMasp.setText(null);
        lblMota.setText(null);
        lblNSX.setText(null);
        lblTenSP.setText(null);
        lblHinh.setToolTipText(model.getHinh());
        if(model.getHinh()!= null){
            lblHinh.setIcon(ShareHelper.readLogo(model.getHinh()));
        }
    }
    
    SanPham getModel(){
        SanPham model = new SanPham();
        model.setMaSP(txtMasp.getText());
        model.setTenSP(txtTensp.getText());
        model.setdVT(txtDvt.getText());
        model.setNuocSX(txtNsx.getText());
        model.setGia(txtGia.getText());
        model.setHinh(lblHinh.getToolTipText());
        model.setGhiChu(txtMota.getText());
        return model;
    }

    void setStatus(boolean insertable){
        btnThem.setEnabled(insertable);
        btnSua.setEnabled(!insertable);
        btnXoa.setEnabled(!insertable);
        boolean first = this.index > 0;
        boolean last = this.index < tblGirlview.getRowCount() - 1;
        btnFisrt.setEnabled(!insertable && first);
        btnPrev.setEnabled(!insertable && first);
        btnNext.setEnabled(!insertable && last);
        btnLast.setEnabled(!insertable && last);
    } 
    
    void selectImage() {
        if(jFileChooser1.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
            File file = jFileChooser1.getSelectedFile();
            if(ShareHelper.saveLogo(file)){
                // Hiển thị hình lên form
                lblHinh.setIcon(ShareHelper.readLogo(file.getName()));
                lblHinh.setToolTipText(file.getName());
            }
        }
    }
