    public boolean checkloi(){
       try{
          if(this.txtMasp.getText().equals("")){
            lblMasp.setText("Chưa nhập mã Sản Phẩm!");
            
              txtMasp.requestFocus();
              return false;
          }else{
              lblMasp.setText(null);
              
          }
          if(this.txtTensp.getText().equals("")){
            lblTenSP.setText("Chưa nhập Tên Sản Phẩm!");
              txtTensp.requestFocus();
              return false;
          }else{
              lblTenSP.setText(null);
          }
          if(this.txtDvt.getText().equals("")){
            lblDVT.setText("Chưa nhập Đơn Vị Tính!");
              txtDvt.requestFocus();
              return false;
          }else{
              lblDVT.setText(null);
          }
          if(this.txtNsx.getText().equals("")){
            lblNSX.setText("Chưa nhập Nước Sản Xuất!");             
              txtNsx.requestFocus();
              return false;
          }else{
              lblNSX.setText(null);
          }
          if(this.txtGia.getText().equals("")){
            lblGia.setText("Chưa nhập Giá Sản Phẩm!");              
              txtGia.requestFocus();
              return false;
          }else{
              lblGia.setText(null);
          }
          if(this.txtMota.getText().equals("")){
            lblMota.setText("Chưa nhập Mô Tả!");    
              txtMota.requestFocus();
              return false;
          }else{
              lblMota.setText(null);
          }
   }catch(Exception e){
                e.printStackTrace();
                return false;  
   }
       return true;
}
