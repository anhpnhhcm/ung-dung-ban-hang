     //Bắt lỗi
    public boolean isvalid(){
        if(txtSoHD.getText().length() == 0){
            DialogHelper.alert(this, "Không được bỏ trống số hóa đơn!");
            return false;
        }
        if(txtSoHD.getText().length() < 2){
            DialogHelper.alert(this, "Số hóa đơn có ít nhất có 2 ký tự!");
            return false;
        }
        if(txtNGLHD.getText().length() < 3){
            DialogHelper.alert(this, "Ngày lập hóa đơn không nhâp đúng định dạng!");
            return false;
        }
        return true;
    }
    
    void load() {
        DefaultTableModel model = (DefaultTableModel) tblGirlview.getModel();
        model.setRowCount(0);
        try {
            List<HoaDon> list = dao.select();
            for (HoaDon tl : list) {
                Object[] row = {
                tl.getSoHD(),
                tl.getMaKH(),
                tl.getMaNV(),
                tl.getMaSP(),
                tl.getSoLuong(),
                tl.getNgLHD(),
                tl.getTriGia(),
                tl.getGhiChu(),           
            };
            model.addRow(row);
            }
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    public boolean insert(){
        HoaDon model = getModel();
        try {
            dao.insert(model);
            this.load();
            this.clear();
            DialogHelper.alert(this, "Thêm mới thành công!");
            return true;
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Thêm mới thất bại!");
        }
        return false;
    }
    
    public boolean update(){
        HoaDon model = getModel();
        try {
            dao.update(model);
            this.load();
            DialogHelper.alert(this, "Cập nhật thành công!");
            return true;
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Cập nhật thất bại!");
        }
        return false;
    }

    public boolean delete(){
        if(DialogHelper.confirm(this, "Bạn có muốn xóa hay không?")){
            String macd = txtSoHD.getText();
            try {
                dao.delete(macd);
                this.load();
                this.clear();
                DialogHelper.alert(this, "Xóa thành công!");
                return true;
            }
            catch (Exception e) {
                DialogHelper.alert(this, "Xóa thất bại!");
            }
        }
        return false;
    }

    void clear(){
        this.setModel(new HoaDon());
        this.setStatus(true);
    }

    void edit() {
        try {
            int macd = (int) tblGirlview.getValueAt(this.index, 0);
            HoaDon model = dao.findById(macd);
            if(model != null){
            this.setModel(model);
            this.setStatus(false);
            }
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    void setModel(HoaDon model){
        txtSoHD.setText(String.valueOf(model.getSoHD()));
        txtMakh.setText(model.getMaKH());
        txtManv.setText(model.getMaNV());
        txtMasp.setText(model.getMaSP());
        txtSL.setText(String.valueOf(model.getSoLuong()));
        txtNGLHD.setText(model.getNgLHD());
        txtTongtien.setText(model.getTriGia());
        txtGhichu.setText(model.getGhiChu());
        lblGC.setText(null);
        lblHD.setText(null);
        lblMakh.setText(null);
        lblManv.setText(null);
        lblMasp.setText(null);
        lblNlhd.setText(null);
        lblTT.setText(null);
        lblsl.setText(null);
    }
    
    HoaDon getModel(){
        HoaDon model = new HoaDon();
        model.setMaKH(txtMakh.getText());
        model.setSoHD(Integer.parseInt(txtSoHD.getText()));
        model.setNgLHD(txtNGLHD.getText());
        model.setMaNV(txtManv.getText());
        model.setMaSP(txtMasp.getText());
        model.setSoLuong(Integer.parseInt(txtSL.getText()));
        model.setTriGia(txtTongtien.getText());
        model.setGhiChu(txtGhichu.getText());
        return model;
    }

    void setStatus(boolean insertable){
        txtSoHD.setEditable(insertable);
        btnThem.setEnabled(insertable);
        btnSua.setEnabled(!insertable);
        btnXoa.setEnabled(!insertable);
        boolean first = this.index > 0;
        boolean last = this.index < tblGirlview.getRowCount() - 1;
        btnFirst.setEnabled(!insertable && first);
        btnPrev.setEnabled(!insertable && first);
        btnNext.setEnabled(!insertable && last);
        btnLast.setEnabled(!insertable && last);
    } 
