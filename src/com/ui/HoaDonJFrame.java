    private void txtSLKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSLKeyReleased
    double SoLuong = 0;
       double Tien = 0;
       try {
           SoLuong = Integer.valueOf(txtSL.getText());
        } catch (Exception e) {
        }
        double Gia = spdao.GetGiaSanPham(txtMasp.getText().toString());
        Tien = (double) Gia * SoLuong;
        txtTongtien.setText(String.valueOf(Tien));
        if(!txtSL.getText().equals("")){
           lblsl.setText(null);
      }
    }//GEN-LAST:event_txtSLKeyReleased

