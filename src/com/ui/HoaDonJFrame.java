    public boolean checkloi(){
       try{
          if(this.txtSoHD.getText().equals("")){
            lblHD.setText("Chưa nhập Số Hóa Đơn!");         
              txtSoHD.requestFocus();
              return false;
          }else{
              lblHD.setText(null);      
          }
          if(this.txtMakh.getText().equals("")){
            lblMakh.setText("Chưa nhập Mã Khách Hàng!");
              txtMakh.requestFocus();
              return false;
          }else{
              lblMakh.setText(null);
          }
          if(this.txtManv.getText().equals("")){
            lblManv.setText("Chưa nhập Mã Nhân Viên!");
              txtManv.requestFocus();
              return false;
          }else{
              lblManv.setText(null);
          }
          if(this.txtMasp.getText().equals("")){
            lblMasp.setText("Chưa nhập Mã Sản Phẩm!");        
              txtMasp.requestFocus();
              return false;
          }else{
              lblMasp.setText(null);
          }
          if(this.txtSL.getText().equals("")){
            lblsl.setText("Chưa nhập Số Lượng!");    
              txtSL.requestFocus();
              return false;
          }else{
              lblsl.setText(null);
          }
          if(this.txtNGLHD.getText().equals("")){
            lblNlhd.setText("Chưa nhập Ngày Lập Hóa Đơn!");            
              txtNGLHD.requestFocus();
              return false;
          }else{
              lblNlhd.setText(null);
          }
          if(this.txtGhichu.getText().equals("")){
            lblGC.setText("Chưa nhập Ghi Chú!");             
              txtGhichu.requestFocus();
              return false;
          }else{
              lblGC.setText(null);
          }
   }catch(Exception e){
                e.printStackTrace();
                return false;  
   }
       return true;
}
