    public boolean checkloi(){
       try{
          if(this.txtMakh.getText().equals("")){
            lblMakh.setText("Chưa nhập Mã Khách Hàng!");
            
              txtMakh.requestFocus();
              return false;
          }else{
              lblMakh.setText(null);       
          }
          if(this.txtHoten.getText().equals("")){
            lblHt.setText("Chưa nhập Họ Tên Khách Hàng!");
              txtHoten.requestFocus();
              return false;
          }else{
              lblHt.setText(null);
          }
          if(this.txtDiachi.getText().equals("")){
            lblDC.setText("Chưa nhập Địa Chỉ!");
              txtDiachi.requestFocus();
              return false;
          }else{
              lblDC.setText(null);
          }
          if(this.txtSodt.getText().equals("")){
            lblSDT.setText("Chưa nhập Số Điện Thoại!");            
              txtSodt.requestFocus();
              return false;
          }else{
              lblSDT.setText(null);
          }
          if(this.txtNgaysinh.getText().equals("")){
            lblNgaySinh.setText("Chưa nhập Ngày Sinh!");           
              txtNgaysinh.requestFocus();
              return false;
          }else{
              lblNgaySinh.setText(null);
          }
   }catch(Exception e){
                e.printStackTrace();
                return false;  
   }
       return true;
}

