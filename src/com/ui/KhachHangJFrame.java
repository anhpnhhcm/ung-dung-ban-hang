    public boolean isvalid(){
        if(txtMakh.getText().length() == 0){
            DialogHelper.alert(this, "Không được bỏ trống mã khách hàng!");
            return false;
        }
        if(txtMakh.getText().length() < 3){
            DialogHelper.alert(this, "Mã khách hàng có ít nhất có 3 ký tự!");
            return false;
        }
        if(txtNgaysinh.getText().length() < 3){
            DialogHelper.alert(this, "Ngày sinh không nhâp đúng định dạng!");
            return false;
        }
        return true;
    }
    
    void load() {
        DefaultTableModel model = (DefaultTableModel) tblGirlview.getModel();
        model.setRowCount(0);
        try {
            List<KhachHang> list = dao.select();
            for (KhachHang tl : list) {
                Object[] row = {
                tl.getMaKH(),
                tl.getHoTen(),
                tl.getDiaChi(),
                tl.getSoDienThoai(),
                tl.getNgaySinh()
            };
            model.addRow(row);
            }
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    public boolean insert(){
        KhachHang model = getModel();
        try {
            dao.insert(model);
            this.load();
            this.clear();
            DialogHelper.alert(this, "Thêm mới thành công!");
            return true;
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Thêm mới thất bại!");
        }
        return false;
    }
    
    public boolean update(){
        KhachHang model = getModel();
        try {
            dao.update(model);
            this.load();
            DialogHelper.alert(this, "Cập nhật thành công!");
            return true;
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Cập nhật thất bại!");
        }
        return false;
    }

    public boolean delete(){
        if(DialogHelper.confirm(this, "Bạn có muốn xóa hay không?")){
            String MaKH = txtMakh.getText();
            try {
                dao.delete(MaKH);
                this.load();
                this.clear();
                DialogHelper.alert(this, "Xóa thành công!");
                return true;
            }
            catch (Exception e) {
                DialogHelper.alert(this, "Xóa thất bại!");
            }
        }
        return false;
    }

    void clear(){
        this.setModel(new KhachHang());
        this.setStatus(true);
    }

    void edit() {
        try {
            String MaKH = (String) tblGirlview.getValueAt(this.index, 0);
            KhachHang model = dao.findById(MaKH);
            if(model != null){
            this.setModel(model);
            this.setStatus(false);
            }
        }
        catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    void setModel(KhachHang model){
        txtMakh.setText(model.getMaKH());
        txtHoten.setText(model.getHoTen());
        txtDiachi.setText(model.getDiaChi());
        txtNgaysinh.setText(model.getNgaySinh());
        txtSodt.setText(model.getSoDienThoai());
        lblDC.setText(null);
        lblHt.setText(null);
        lblMakh.setText(null);
        lblNgaySinh.setText(null);
        lblSDT.setText(null);
    }
    
    KhachHang getModel(){
        KhachHang model = new KhachHang();
        model.setMaKH(txtMakh.getText());
        model.setHoTen(txtHoten.getText());
        model.setDiaChi(txtDiachi.getText());
        model.setNgaySinh(txtNgaysinh.getText());
        model.setSoDienThoai(txtSodt.getText());
        return model;
    }

    void setStatus(boolean insertable){
        btnThem.setEnabled(insertable);
        btnSua.setEnabled(!insertable);
        btnXoa.setEnabled(!insertable);
        boolean first = this.index > 0;
        boolean last = this.index < tblGirlview.getRowCount() - 1;
        btnFirst.setEnabled(!insertable && first);
        btnPrev.setEnabled(!insertable && first);
        btnNext.setEnabled(!insertable && last);
        btnLast.setEnabled(!insertable && last);
    } 
   
